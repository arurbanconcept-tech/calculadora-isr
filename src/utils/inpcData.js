// Devuelve el INPC del mes anterior al indicado (como requiere Art. 124 LISR
// para el mes de enajenación — usa el mes inmediato anterior)
export function obtenerINPC(mesAnio) {
  return INPC_MENSUAL[mesAnio] ?? null;
}

// Devuelve la clave del mes inmediato anterior
export function mesPrevio(mesAnio) {
  const [anio, mes] = mesAnio.split('-').map(Number);
  if (mes === 1) return `${anio - 1}-12`;
  return `${anio}-${String(mes - 1).padStart(2, '0')}`;
}

// Lista de meses disponibles para los selectores
export function getMesesDisponibles() {
  return Object.keys(INPC_MENSUAL).sort();
}

